                  <h5>8 释义</h5>
                   <p>8.1 手术意外、麻醉意外</p>
                   <p>手术意外：</p>
                   <p>指实施手术过程中发生的由于患者的病情或患者体质的特殊性而发生难以预
                  料和防范的不良后果。</p>
                   <p>麻醉意外：</p>
                   <p>是指正常施行医疗麻醉时出现的由于该麻醉造成的意外反应。</p>
                  <h6>8.2 保险金申请人</h6>
                   <p>身故保险金申请人是指受益人或被保险人的继承人或依法享有保险金请求权
                  的其他自然人；残疾保险金申请人是指被保险人。</p>
