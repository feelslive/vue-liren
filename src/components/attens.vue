                   <p>合同争议解决方式由当事人在合同约定从下列两种方式中选择一种：</p>
                   <p>（1）因履行本合同发生的争议，由当事人协商解决，协商不成的，提交保险
                  单载明的仲裁委员会仲裁；</p>
                   <p>（2）因履行本合同发生的争议，由当事人协商解决，协商不成的，依法向人
                  民法院起诉。</p>
